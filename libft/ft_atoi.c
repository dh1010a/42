#include "libft.h"

int	ft_atoi(const char *str)
{
	int		sign;
	int		cnt;
	size_t	i;

	sign = 1;
	cnt = 0;
	i = 0;
	while ((str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' '))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		cnt = (cnt * 10) + (str[i] - '0');
		i++;
	}
	return (sign * cnt);
}
